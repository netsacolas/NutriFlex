// Função de busca otimizada
export function searchFoods(query: string, limit: number = 10): string[] {
  if (!query || query.trim().length === 0) {
    return [];
  }

  const searchTerm = query.toLowerCase().trim();

  // Resultados que começam com o termo de busca (prioridade alta)
  const startsWithResults = FOOD_DATABASE.filter(food =>
    food.toLowerCase().startsWith(searchTerm)
  );

  // Resultados que contêm o termo de busca mas não começam com ele
  const containsResults = FOOD_DATABASE.filter(food => {
    const lowerFood = food.toLowerCase();
    return lowerFood.includes(searchTerm) && !lowerFood.startsWith(searchTerm);
  });

  // Combinar resultados priorizando os que começam com o termo
  const allResults = [...startsWithResults, ...containsResults];

  // Limitar o número de resultados
  return allResults.slice(0, limit);
}
