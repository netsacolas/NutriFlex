/**
 * Retorna a dica do dia baseada na data atual
 * A mesma dica é exibida durante todo o dia
 * @returns string com a dica do dia
 */
export const getTipOfTheDay = (): string => {
  // Usar a data atual (ano + mês + dia) como semente para selecionar a dica
  const today = new Date();
  const year = today.getFullYear();
  const month = today.getMonth() + 1; // 0-11, então +1
  const day = today.getDate();

  // Criar um número único para o dia (ex: 20250126)
  const dateNumber = year * 10000 + month * 100 + day;

  // Usar módulo para selecionar uma dica (garante que seja sempre a mesma no dia)
  const tipIndex = dateNumber % dailyTips.length;

  return dailyTips[tipIndex];
};

/**
 * Retorna uma dica aleatória (para uso em outras partes do app)
 * @returns string com uma dica aleatória
 */
export const getRandomTip = (): string => {
  const randomIndex = Math.floor(Math.random() * dailyTips.length);
  return dailyTips[randomIndex];
};

/**
 * Retorna estatísticas sobre o banco de dicas
 */
